public class Point {
    int x, y;

    public Point(int x_user, int y_user) {
        this.x = x_user;
        this.y = y_user;
    }

    public boolean isEmpty(int[][] map){
        boolean answer = false;
        if (map[x][y] >= 0){
            answer = true;
        }
        return answer;
    }
}